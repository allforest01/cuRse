v1 = 1:5
v2 = 3:8

m1 <- cbind(v1, v2)
m1
# output:
#      v1 v2
# [1,]  1  3
# [2,]  2  4
# [3,]  3  5
# [4,]  4  6
# [5,]  5  7
# [6,]  1  8

m2 <- rbind(v1, v2)
m2
# output:
#    [,1] [,2] [,3] [,4] [,5] [,6]
# v1    1    2    3    4    5    1
# v2    3    4    5    6    7    8

print(dim(m1))
# output:
# [1] 6 2

print(dim(m2))
# output:
# [1] 2 6

print(t(m1))
# output:
#    [,1] [,2] [,3] [,4] [,5] [,6]
# v1    1    2    3    4    5    1
# v2    3    4    5    6    7    8

m3 = rbind(matrix(2, 2, 2), c("a", "b"))
m3
# output:
#      [,1] [,2]
